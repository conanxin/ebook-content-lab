def point(name: str) -> dict:
    return {
        "name": name,
        "lat": None,
        "lng": None,
        "coordinate_source": None,
        "coordinate_confidence": "missing",
    }


def seg(
    order: int,
    chapter: str,
    title: str,
    start: str,
    end: str,
    via: list[str],
    summary: str,
    directions: list[str],
    refs: list[dict],
    terrain: str | None = None,
    roads: str | None = None,
    water: str | None = None,
    resupply: str | None = None,
    lodging: str | None = None,
    risks: str | None = None,
    confidence: str = "needs_review",
    notes: list[str] | None = None,
) -> dict:
    return {
        "id": f"seg-{order:03d}",
        "order": order,
        "chapter": chapter,
        "title": title,
        "start": point(start),
        "end": point(end),
        "via": [point(name) for name in via],
        "distance_km_book": None,
        "distance_km_computed": None,
        "route_summary": summary,
        "walking_directions": directions,
        "terrain": terrain,
        "roads_or_paths": roads,
        "water_sources": water,
        "resupply": resupply,
        "lodging": lodging,
        "risks_or_notes": risks,
        "book_refs": refs,
        "confidence": confidence,
        "review_notes": notes or [],
    }


